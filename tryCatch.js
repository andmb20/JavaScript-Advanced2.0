/*
    TRATAMENTO DE ERROS ( TRY CATCH e FINALLY)
    Importante para ter um código mais resiliente

    try-> Envolver um trecho de código que pode ocorrer um erro, o controle é tranferido para o bloco catch mais próximo.
    catch-> Especifica uma resposta ou roda outro código caso o try não tenha rodado
    finally-> Opcional, caso queira executar um código após rodar o try ou catch 

    try{
        todo código da aplicação
    } catch(parametro de erro){
        retornar erro ou status de erro 
    } finally {
        Opcional, caso queira executar um código após rodar o try ou catch 
    }
*/

